;********************************************************************************************************
;                                           PUBLIC FUNCTIONS
;********************************************************************************************************

    .global     _set_ipl
    .global     _get_ipl

    .text


;********************************************************************************************************
;                                        set_ipl() & get_ipl()
;
; Description: Set or retrieve interrupt priority level.
;********************************************************************************************************

_set_ipl:
    PUSH R2
    MVFC PSW, R2
    AND  #-0F000001H, R2
    SHLL #24, R1
    OR   R1, R2
    MVTC R2, PSW
    POP  R2
    RTS

_get_ipl:
    MVFC PSW, R1
    SHLR #24, R1
    AND  #15, R1
    RTS


    .END
